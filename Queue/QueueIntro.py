'''
Queue is a linear data structure that follows FIFO order/principle
insertion takes place frome the rear end 
deletion can take place from front end
Ex.Queue at ticket counters

4-major operations :
    enqueue(ele):used to insert element at rear(tail) end
    dequeue():removes the element from the head(front) end
    peekfirst():to get the first ele of the queue
    peeklast():to get  the last ele of the queue

all operations works in constant time--O(n)

Applications:
    scheduling
    maintaining playlist
    interrupt handling 

'''